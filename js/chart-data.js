		    function tween( b, callback ) {
		      return function( a ) {
		        var i = (function interpolate() {
		          return function( t ) {
		            return a.map( function( datum, index ) {
		              return {
		                date  : datum.date,
		                value : datum.value + b[ index ].value * t
		              };
		            } ); 
		          };
		        })();
		  
		        return function( t ) {
		          return callback( i ( t ) );
		        };
		      };
		    }
